public class Unicode {
    public static void main(String[] args) {
        int decimalOne = 1;
        System.out.println("decimal one= " + decimalOne);
        int binaryThree = 0b11;
        System.out.println("binary three: " + binaryThree);
        int octalEight = 010;
        System.out.println("Octaleight: " + octalEight);
        int hexTen = 0xA;
        System.out.println("hexten: " + hexTen);
        int UnicodeValueOfOne = '1';
        System.out.println("Unicode value of one: " + UnicodeValueOfOne);
        int UnicodeValueOfA = 'A';
        System.out.println("Unicode value of A: " + UnicodeValueOfA);
        int UnicodeValueOfZ = 'Z';
        System.out.println("Unicode value of Z: " + UnicodeValueOfZ);
        System.out.println("Hello:" + ' ' + "jk");
        System.out.println();
        // Unicode is always declared in the single quotes


        
    }
}
